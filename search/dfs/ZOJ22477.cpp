int geth()
{
    int ans = 0;
    for(int i=0 ;i<6 ;i++)
    {
        for(int j=0 ;j<9 ;j++)
        if( a[row[i][j]]!= a[centre[i]])
            ans++;
    }
    return ans ;
}
int dfs( int d)
{
    int h= geth();
    if(d+ceil( h/12.0)>depth) return 0;

    if(!h) return 1;
    char maze[100];
    for(int i=0 ;i<12 ;i++)
    {
        memcpy(maze ,a,sizeof(a));
        for(int j=0 ;j<20 ;j++)
            a[change[i][j]] = maze[change[i^1][j]];
        
        ans[d] = i/2;
        if(i&1) dir[d]=-1;
        else dir[d] =1;
        if( dfs(d+1 )) return 1;

        memcpy(a,maze, sizeof(maze)); 
    }
    return 0;

}
